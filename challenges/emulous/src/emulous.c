/*
 * emulous - PowerPC reversing challenge
 *
 * Behavior:
 * - prompts for a "password"
 * - rejects unless the exact word exists in /usr/share/dict/words
 * - uses the word as a key to decrypt an embedded ciphertext
 * - if the decrypted output is printable ASCII, prints it; otherwise prints:
 *     "Sorry no printable output!"
 *
 * The shipped binary is intentionally dynamically linked (students must provide
 * the right PPC loader/libc when running under qemu-user) and its ELF e_ident
 * bytes are patched to avoid binfmt_misc auto-qemu.
 */

